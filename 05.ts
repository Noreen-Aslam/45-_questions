// Store the name with whitespace characters in a variable
let personNameWithWhitespace: string = "\t  Noreen \n";

// Output the name with whitespace characters
console.log("Name with Whitespaces:", personNameWithWhitespace);

// Remove leading and trailing whitespace characters from the name and store it in another variable
let strippedName: string = personNameWithWhitespace.trim();

// Output the stripped name
console.log("Stripped Name:", strippedName);
